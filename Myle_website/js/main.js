function projectLinks(project) {
  const links = [];
  if (project.figmaUrl) {
    links.push(`<a class="project-live-link" href="${project.figmaUrl}" target="_blank" rel="noopener">Open Figma prototype</a>`);
  }
  if (project.desktopPrototypeUrl) {
    links.push(`<a class="project-live-link" href="${project.desktopPrototypeUrl}" target="_blank" rel="noopener">Open desktop prototype</a>`);
  }
  if (project.mobilePrototypeUrl) {
    links.push(`<a class="project-live-link" href="${project.mobilePrototypeUrl}" target="_blank" rel="noopener">Open mobile prototype</a>`);
  }
  if (project.liveUrl) {
    links.push(`<a class="project-live-link" href="${project.liveUrl}" target="_blank" rel="noopener">Open live project</a>`);
  }
  if (project.presentationUrl) {
    links.push(`<a class="project-live-link" href="${project.presentationUrl}" target="_blank" rel="noopener">Open presentation</a>`);
  }
  if (project.processUrl) {
    links.push(`<a class="project-live-link" href="${project.processUrl}" target="_blank" rel="noopener">Open process page</a>`);
  }
  if (project.prototypeUrl) {
    links.push(`<a class="project-live-link" href="${project.prototypeUrl}" target="_blank" rel="noopener">Open first prototype</a>`);
  }
  if (project.processJournalUrl) {
    links.push(`<a class="project-live-link" href="${project.processJournalUrl}" target="_blank" rel="noopener">Open process journal</a>`);
  }
  if (project.layoutUrl) {
    links.push(`<a class="project-live-link" href="${project.layoutUrl}" target="_blank" rel="noopener">Open layout designs</a>`);
  }
  return links.length ? `<div class="project-link-row">${links.join("")}</div>` : "";
}

